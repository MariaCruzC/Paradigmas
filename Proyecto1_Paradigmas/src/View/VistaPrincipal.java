    private void jMenuItem_AbrirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem_AbrirActionPerformed
        JFileChooser fc = new JFileChooser();
        fc.showOpenDialog(null);       
        File archivo = fc.getSelectedFile();
        try {
            if (archivo.exists()) {
                FileReader fr = new FileReader(archivo);
                BufferedReader br = new BufferedReader(fr);
                String linea;
                while ((linea = br.readLine()) != null) {
                    control.agregar(linea);
                }//cierre while
                br.close();
            } else {
                System.out.println("El archivo no existe");
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        System.out.println("Lectura COMPLETA");
        control.imprimir();
        //debo de cerrar el file
    }//GEN-LAST:event_jMenuItem_AbrirActionPerformed

